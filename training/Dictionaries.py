print("Dictionaries")
# A dictionary consists of key value pairs. ex.
# "name" is the key and "Nathaniel" is the value.
# It has to be wrote in this format.

person = {
	"name": "Nathaniel",
	"age": 28,
	"address": "USA"
	
}

print(person["name"])
print(person["age"])
print(person.get("age"))
print(person["address"])

print("Key Values")
print(person.keys())
print(person.values())
